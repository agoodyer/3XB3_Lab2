def BFS3(G, node1):

    predecessor_dictionary = dict() 
    Q = deque([node1])
    marked = {node1 : True}
    for node in G.adj:
        if node != node1:
            marked[node] = False
    while len(Q) != 0:
        current_node = Q.popleft()
        for node in G.adj[current_node]:
            if not marked[node]:
                Q.append(node)
                predecessor_dictionary[node] = current_node
                marked[node] = True

    return predecessor_dictionary



